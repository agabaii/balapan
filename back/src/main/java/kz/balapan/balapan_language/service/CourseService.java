    public void initializeCourses() {
        if (courseRepository.findByLanguageCode("kk").isEmpty()) {
            Language kazakh = languageService.getLanguageByCode("kk");
            createKazakhCourse(kazakh);
        }

        if (courseRepository.findByLanguageCode("ru").isEmpty()) {
            Language russian = languageService.getLanguageByCode("ru");
            createRussianCourse(russian);
        }

        if (courseRepository.findByLanguageCode("en").isEmpty()) {
            Language english = languageService.getLanguageByCode("en");
            createEnglishCourse(english);
        }

        if (courseRepository.findByLanguageCode("interactive").isEmpty()) {
            // Create dummy language for interactive course if needed, or re-use en/kk
            // For simplicity, we assume "interactive" is a special code
            createInteractiveCourse();
        }
    }

